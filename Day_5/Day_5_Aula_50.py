# Usado para fazer que atividades se repitam até que um critério seja atendido.

# For Loop

# for item in list_of_items
    #Do something to each item

# Exemplo com lista
fruits = ["Apple", "Peach", "Pear"]

for fruit in fruits:  
    print(fruit)
    print(fruit + " pie!") # Importante perceber a identação, até aqui o código está dentro do loop.
print(fruits) # Aqui está fora do loop.

# o fruit equivale ao i, seria algo como para cada fruit na list fruit faça algo.

# Um loop executa uma linha de código um determinado número de vezes ou até que
# uma condição seja atendida.


